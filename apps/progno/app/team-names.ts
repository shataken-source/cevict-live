/**
 * Get teams for a specific sport
 */
export function getTeamsForSport(sport: string): readonly string[] {
  const sportUpper = sport.toUpperCase();
  if (sportUpper === "NFL" || sportUpper === "FOOTBALL") {
    return NFL_TEAMS;
  } else if (sportUpper === "MLB" || sportUpper === "BASEBALL") {
    return MLB_TEAMS;
  } else if (sportUpper === "NBA" || sportUpper === "BASKETBALL") {
    return NBA_TEAMS;
  } else if (sportUpper === "NHL" || sportUpper === "HOCKEY") {
    return NHL_TEAMS;
  } else if (sportUpper === "NCAAF" || sportUpper === "NCAA FOOTBALL" || (sportUpper.includes("NCAA") && sportUpper.includes("FOOTBALL"))) {
    return NCAAF_TEAMS;
  } else if (sportUpper === "NCAAB" || sportUpper === "NCAA BASKETBALL" || (sportUpper.includes("NCAA") && sportUpper.includes("BASKETBALL"))) {
    return NCAAB_TEAMS;
  } else if (sportUpper === "NCAA" || sportUpper === "COLLEGE") {
    // Default to football for generic NCAA
    return NCAAF_TEAMS;
  }
  // Default to NFL
  return NFL_TEAMS;
}

/**
 * Normalize a team name to the standard format
 */
export function normalizeTeamName(input: string, sport?: string): string {
  if (!input) return "";

  const normalized = input.trim();
  const teams = sport ? getTeamsForSport(sport) : [...NFL_TEAMS, ...MLB_TEAMS, ...NBA_TEAMS, ...NHL_TEAMS, ...NCAA_D1_TEAMS];

  // Check if it's already a standard name
  if (teams.includes(normalized as any)) {
    return normalized;
  }

  // Check aliases
  const alias = TEAM_ALIASES[normalized];
  if (alias) {
    return alias;
  }

  // Try case-insensitive match
  const lowerInput = normalized.toLowerCase();
  for (const team of teams) {
    if (team.toLowerCase() === lowerInput) {
      return team;
    }
  }

  // Try partial match
  for (const team of teams) {
    if (team.toLowerCase().includes(lowerInput) || lowerInput.includes(team.toLowerCase())) {
      return team;
    }
  }

  // Return original if no match found
  return normalized;
}

